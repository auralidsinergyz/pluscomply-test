	/**
	 * @param $string
	 *
	 * @return string|string[]
	 */
	public static function remove_special_character( $string, $validate_only = false ) {

		$t = $string;

		$specChars = array(
			'!'     => '',
			'"'     => '',
			'#'     => '',
			'$'     => '',
			'%'     => '',
			'&amp;' => '',
			//'\''    => '',
			'('     => '',
			')'     => '',
			'*'     => '',
			'+'     => '',
			','     => '',
			'â‚¹'     => '',
			'.'     => '',
			'/-'    => '',
			':'     => '',
			';'     => '',
			'<'     => '',
			'='     => '',
			'>'     => '',
			'?'     => '',
			'@'     => '',
			'['     => '',
			'\\'    => '',
			']'     => '',
			'^'     => '',
			'_'     => '',
			'`'     => '',
			'{'     => '',
			'|'     => '',
			'}'     => '',
			'~'     => '',
			'-----' => '-',
			'----'  => '-',
			'---'   => '-',
			'/'     => '',
			'--'    => '-',
			'/_'    => '-',

		);

		if ( $validate_only ) {
			foreach ( $specChars as $k => $v ) {
				if ( false !== strpos( $t, $k ) ) {
					return false;
				}
			}
		}
		foreach ( $specChars as $k => $v ) {
			$t = str_replace( $k, $v, $t );
		}

		return $t;
	}

